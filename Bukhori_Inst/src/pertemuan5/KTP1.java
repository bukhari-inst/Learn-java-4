            bufferedWriter.newLine();
            bufferedWriter.write(kec.getText());
            bufferedWriter.newLine();
            bufferedWriter.write(agama.getText());
            bufferedWriter.newLine();
            bufferedWriter.write(kawin.getText());
            bufferedWriter.newLine();
            bufferedWriter.write(kerja.getText());
            bufferedWriter.newLine();
            bufferedWriter.write(wni.getText());
            bufferedWriter.newLine();
