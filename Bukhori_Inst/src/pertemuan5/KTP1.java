           public void show(String filetext) {
        String baris = null;
        //filereader adalah clas/objek
        FileReader Reader = null;
        try {
            Reader = new FileReader(filetext);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(KTP1.class.getName()).log(Level.SEVERE, null, ex);
        }
        //menampilkan file
        BufferedReader buffredReader2 = new BufferedReader(Reader);
        try {
            int bariske = 0;
            while ((baris = buffredReader2.readLine()) != null) {
                System.out.println(baris);

                if (bariske == 0) {
                    nik.setText(baris);
                } else if (bariske == 1) {
                    nama.setText(baris);
                } else if (bariske == 2) {
                    ttl.setText(baris);
                } else if (bariske == 3) {
                    foto1.setIcon(new javax.swing.ImageIcon(getClass().getResource(baris)));
                    pathfoto.setText(baris);
                } else if (bariske == 4) {
                    alamat.setText(baris);
                } else if (bariske == 5) {
                    rtrw.setText(baris);
                } else if (bariske == 6) {
                    kel.setText(baris);
                } else if (bariske == 7) {
                    kec.setText(baris);
                } else if (bariske == 8) {
                    agama.setText(baris);
                } else if (bariske == 9) {
                    kawin.setText(baris);
                } else if (bariske == 10) {
                    kerja.setText(baris);
                } else if (bariske == 11) {
                    wni.setText(baris);
                }
                bariske++;
            }
        } catch (IOException ex) {
            Logger.getLogger(KTP1.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            buffredReader2.close();
        }catch (IOException ex) {
            Logger.getLogger(KTP1.class.getName()).log(Level.SEVERE,null, ex);
        }
    }

